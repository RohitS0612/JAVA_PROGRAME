import java.util.*;
public class HalfStar {
	// Java program to print half pyramid pattern using stars
	
	public class Main
	{
	public static void main(String[] args)
	{
	int n, i, j;
	Scanner sc = new Scanner(System.in);
	System.out.println("enter the n");
	n = sc.nextInt();
	for(i = 0; i < n; i++)
	{
	for(j = 0; j <= i; j++)
	{
	System.out.print("");
	}
	System.out.print("\n");
	}
	}
	}

}
